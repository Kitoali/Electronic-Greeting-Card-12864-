void gundong()
{
	int page = 0, col = 0, zishu = 0; //zishu用来统计显示的字数

	init();
	clear_screen(0);
	delay2(0);
	select_screen(1);

	while (1){
		hz_disp16( page, col, dat[zishu]);
		delay2(0);
		zishu++;

		col = col + 16;//显示一个汉字后列数加16,当超过64,回0
		if (col == 64)
			col = 0;

		if (!(zishu % 4)) //当显示的字数为4,12,28时在左半屏
		{
			select_screen(2);
		}

		if (!(zishu % 8)) //当显示的字数为8,16,32时在右半屏,并且右半屏显示完之后,页数加2。因为汉字占用的空间为16*16，8行为一页
		{
			select_screen(1);
			page += 2;
		}

		if (!(zishu % 32))
		{
			st_line += 8; //当显示完32个字的时候，起始行加一，达到滚动效果

			if (st_line >= 0xff) //如果起始行超过最尾部，则重新置数
				st_line = 0xc0;

			wcmd(st_line + 0); // 设置右半屏起始行

			select_screen(2);
			wcmd(st_line + 0); //设置左半屏起始行

			zishu = 0; page = 0; //将字数和页数都清0

			select_screen(1); //选中左半屏
		}


	}

}

