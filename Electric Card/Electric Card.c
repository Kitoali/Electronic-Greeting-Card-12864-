void busy()// 忙检测函数
{
	P0 = 0X00; // 这句不能少 RS=0;
	RW = 1; E = 1;
	while (P0 & 0X80);//P0&0X80  结果为真则一直执行空操作，也就是说	P0&0X80 为 0 则结束循环
	E = 0;
}
void wcmd(uchar cmd)
{
	busy(); // 每次写入指令或数据之前都进行忙检测 
	RS=0;
	RW = 0;
	P0 = cmd;
	E = 1; // 在 E 下降沿写入数据和指令
	E=0;
}
void wdata(uchar dat) // 写命令和写数据只在	RS=0或 RS=1上不同，其余都相同
{
	busy(); RS = 1; 
	RW = 0;
	P0 = dat;
	E = 1;  // 在 E 下降沿写入数据和指令
	E=0;
}

void set_page(uchar page)// 设置页， 12864LCD共有 8 页，每页有	8 行点阵点。
	{
		page = 0xb8 | page;	// 首页地址为	0XB8
		wcmd(page);	//page 取值范围为	0~7,表示第 1 到 8 页
	}
void set_line(uchar line)//  设置显示的起始行，共有	0―― 63 行，一般从	0 行开始显示
	{line = 0xc0 | line;	// 起始行地址	0XC0
	wcmd(line);	//line  取值范围为	0~63，表示第 1 到 64 行
	}
void set_column(uchar column)// 设置显示的列
	{//column=column&0x3f; //	列的最大值为	0X3F，即 64
		column = 0x40 | column;	// 列的首地址为 0X40,
		wcmd(column);	//column  的取值范围为	0~63，共
	}
void set_onoff(uchar onoff)//	设置显示开关， onoff 取值为 0 或 1
	{
		onoff |= 0x3e;//0X3E	是关显示， 0X3F是开显示
		wcmd(onoff);//  所以若 onoff 为 0，则表示关显示，	onoff 为 1，则表示开显示
	}
void select_screen(uchar screen)// 选屏， screen 取值范围为	0,1,2
	{
		switch (screen)

		{
		case 0:CS1 = 0; CS2 = 0; break; // 全  屏，左右两个屏幕显示同样的内容
		case 1:CS1 = 0; CS2 = 1; break; // 左半屏
		case 2:CS1 = 1; CS2 = 0;  break; // 右半屏
		default:break;
		}
	}
void clear_screen(uchar screen)// 清屏
	{
		uchar i, j;
		select_screen(screen);// 先选屏
		for (i = 0; i < 8; i++)// 控制页数 0―― 7，共 8 页
		{
			set_page(i);// 设置页
			set_column(0); // 设置列，每页都从第	1 列开始，共	64 列
			for(j=0;j<64;j++)// 控制列数 0―― 63，共 64 列
			wdata(0x00);// 写入 0，列地址指针会自动加	1
		}
	}
void init()	//LCD 初始化
	{
		busy(); // 忙检测
		select_screen(0);// 选屏，全屏
		set_onoff(0);		// 关显示
		select_screen(0); // 选屏，全屏 
		set_onoff(1);				// 开显示
		select_screen(0); // 选屏，全屏 
		clear_screen(0);		// 清屏
		set_line(0);	// 起始行： 0
	}
void show(uchar screen, uchar page, uchar column, uchar p[])// 显示图片一个图有	8*128=1024 字节数据
	{
		uchar i, j;	//screen 表示选择屏幕，	page 表示页， column 表示列， *p 表示汉字的数组
		select_screen(screen); // 选屏
			for (i = 0; i < 8; i++) {
		// 设置页，写第一页
			set_column(column);// 设置列
			for (j = 0; j <128 ; j++) // 控制 每一页的 128 列的数据输出
				if(j<64){
					select_screen(1);
				set_page(page+i);
				wdata(p[j + i * 128]);	// 写入该页的数据
				}
				else{
				select_screen(2);
					set_page(page+i);
				wdata(p[j + i * 128]);
				}					
		}		
	}

