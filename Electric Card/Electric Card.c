bit hitkey()
 {
 uchar scancode,keycode;
 scancode=0x0f;  //P1.4~P1.7输出全0
 P1=scancode;   //扫描按键，换回键值
 keycode=P1;  //读P1.0~P1.3的状态
 if((keycode&0x0f)==0x0f)  //屏蔽高4位
 return(0);   //全1则无按键闭合，换回0
 else
 return(1);     //全0则有按键闭合，换回1
 }
